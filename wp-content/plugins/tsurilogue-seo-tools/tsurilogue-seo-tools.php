/**
 * Convert WordPress origin URLs to the public TSURILOGUE-MEDIA URL.
 *
 * @param string $url URL generated by WordPress, JIN, or SEO SIMPLE PACK.
 * @return string
 */
function tsurilogue_seo_tools_convert_public_url( $url ) {
	if ( ! is_string( $url ) || '' === $url ) {
		return $url;
	}

	$origin = untrailingslashit( TSURILOGUE_SEO_TOOLS_ORIGIN_URL );
	$public = untrailingslashit( TSURILOGUE_SEO_TOOLS_PUBLIC_URL );

	if ( 0 === strpos( $url, $public ) ) {
		return tsurilogue_seo_tools_normalize_public_url( $url );
	}

	if ( 0 !== strpos( $url, $origin ) ) {
		return $url;
	}

	$path = substr( $url, strlen( $origin ) );
	$path = '/' . ltrim( (string) $path, '/' );

	return tsurilogue_seo_tools_normalize_public_url( trailingslashit( $public ) . ltrim( $path, '/' ) );
}

/**
 * Normalize accidental duplicate slashes immediately below the public media URL.
 *
 * @param string $url Public URL.
 * @return string
 */
function tsurilogue_seo_tools_normalize_public_url( $url ) {
	$public = trailingslashit( untrailingslashit( TSURILOGUE_SEO_TOOLS_PUBLIC_URL ) );

	return str_replace( $public . '/', $public, $url );
}

/**
 * Convert frontend home URLs while leaving WordPress system paths untouched.
 *
 * @param string      $url         Full home URL.
 * @param string      $path        Path relative to the site root.
 * @param string|null $orig_scheme Requested scheme.
 * @param int|null    $blog_id     Blog ID.
 * @return string
 */
function tsurilogue_seo_tools_convert_frontend_home_url( $url, $path = '', $orig_scheme = null, $blog_id = null ) {
	unset( $orig_scheme, $blog_id );

	if ( is_admin() ) {
		return $url;
	}

	$path = '/' . ltrim( (string) $path, '/' );

	if ( preg_match( '#^/(wp-admin|wp-login\.php|wp-json|xmlrpc\.php|wp-content|wp-includes)(/|$)#', $path ) ) {
		return $url;
	}

	if ( '/' === $path ) {
		return untrailingslashit( TSURILOGUE_SEO_TOOLS_PUBLIC_URL );
	}

	return tsurilogue_seo_tools_convert_public_url( $url );
}

/**
 * Add a frontend guard for links generated outside standard WordPress filters.
 *
 * @return void
 */
function tsurilogue_seo_tools_output_public_url_guard() {
	if ( is_admin() ) {
		return;
	}

	?>
	<script>
		(function () {
			var origin = <?php echo wp_json_encode( untrailingslashit( TSURILOGUE_SEO_TOOLS_ORIGIN_URL ) ); ?>;
			var publicUrl = <?php echo wp_json_encode( untrailingslashit( TSURILOGUE_SEO_TOOLS_PUBLIC_URL ) ); ?>;

			function convertUrl(url) {
				if (!url || url.indexOf(origin) !== 0) {
					return url;
				}

				return publicUrl + "/" + url.slice(origin.length).replace(/^\/+/, "");
			}

			document.querySelectorAll("a[href]").forEach(function (anchor) {
				anchor.href = convertUrl(anchor.href);
			});

			document.querySelectorAll("form[action]").forEach(function (form) {
				form.action = convertUrl(form.action);
			});
		})();
	</script>
	<?php
}

